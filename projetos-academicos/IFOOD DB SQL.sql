-- Criação e utilização do banco de dados do aplicativo
CREATE DATABASE IF NOT EXISTS IFOOD_DB;
USE IFOOD_DB;

-- Criação das tabelas:	
-- Tabela do cliente:
CREATE TABLE IF NOT EXISTS CLIENTE
(
IDCLIENTE INT NOT NULL AUTO_INCREMENT,
CPFCLIENTE CHAR(11) NOT NULL UNIQUE,
NOMECLIENTE VARCHAR(100) NOT NULL,
EMAILCLIENTE VARCHAR(60) NOT NULL,
NUMTELEFONE CHAR(11) NOT NULL,
PRIMARY KEY (IDCLIENTE)
);

-- Tabela do Entregador:
CREATE TABLE IF NOT EXISTS ENTREGADOR
(
IDENTREGADOR INT NOT NULL AUTO_INCREMENT,
CPFENTREGADOR CHAR(11) NOT NULL UNIQUE,
TELEFONEENTREGADOR CHAR(11) NOT NULL,
PRIMARY KEY (IDENTREGADOR)
);

-- Tabela do Veiculo do entregador para ser relacionada ao entregador:
CREATE TABLE IF NOT EXISTS VEICULO
(
TIPOVEICULO ENUM('MOTO','CARRO','BICICLETA') NOT NULL DEFAULT('MOTO'),
PLACAVEICULO CHAR(7) NOT NULL,
PRIMARY KEY (PLACAVEICULO),
IDENTREGADOR INT NOT NULL,
CONSTRAINT FK_VEICULO_ENTREGADOR FOREIGN KEY (IDENTREGADOR) REFERENCES ENTREGADOR(IDENTREGADOR)
);

-- Tabela Dos Restaurantes do aplicativo:
CREATE TABLE IF NOT EXISTS RESTAURANTE
(
IDRESTAURANTE INT NOT NULL AUTO_INCREMENT,
NUMCNPJ CHAR(14) NOT NULL UNIQUE,
NOMERESTAURANTE VARCHAR(100) NOT NULL,
TIPOCULINARIA VARCHAR(50) NOT NULL,
PRIMARY KEY(IDRESTAURANTE)
);

-- Tabela dos Produtos do Restaurante
CREATE TABLE IF NOT EXISTS PRODUTO
(
IDPRODUTO INT NOT NULL AUTO_INCREMENT,
NOMEPRODUTO VARCHAR(60) NOT NULL,
DESCRICAOPRODUTO VARCHAR(150) NOT NULL,
PRECOUNITARIO DECIMAL(5,2) NOT NULL,
IDRESTAURANTE INT NOT NULL,
PRIMARY KEY (IDPRODUTO),
CONSTRAINT FK_PRODUTO_RESTAURANTE FOREIGN KEY (IDRESTAURANTE) REFERENCES RESTAURANTE(IDRESTAURANTE)
);

-- Tabela dos Pedidos
CREATE TABLE IF NOT EXISTS PEDIDO
(
IDPEDIDO INT NOT NULL AUTO_INCREMENT,
HORASAIDA DATETIME NOT NULL,
HORACHEGADA DATETIME NOT NULL,
VALORTOTAL DECIMAL(5,2), -- Taxa de entrega somada ao valor do item
IDCLIENTE INT NOT NULL,
IDENTREGADOR INT NOT NULL,
PRIMARY KEY(IDPEDIDO),
CONSTRAINT FK_PEDIDOS_CLIENTE FOREIGN KEY(IDCLIENTE) REFERENCES CLIENTE(IDCLIENTE),
CONSTRAINT FK_PEDIDOS_ENTREGADOR FOREIGN KEY(IDENTREGADOR) REFERENCES ENTREGADOR(IDENTREGADOR)
);

-- Tabela de Item Pedido
CREATE TABLE IF NOT EXISTS ITEM_PEDIDO
(
IDITEM INT NOT NULL AUTO_INCREMENT,
IDPEDIDO INT NOT NULL,
IDPRODUTO INT NOT NULL,
QUANTIDADE INT NOT NULL,
PRECOUNITARIO DECIMAL(5,2) NOT NULL,
PRIMARY KEY (IDITEM),
CONSTRAINT FK_ITEMPEDIDO_PEDIDO FOREIGN KEY (IDPEDIDO) REFERENCES PEDIDO(IDPEDIDO),
CONSTRAINT FK_ITEMPEDIDO_PRODUTO FOREIGN KEY (IDPRODUTO) REFERENCES PRODUTO(IDPRODUTO)
);


